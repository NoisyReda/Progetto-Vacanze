    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jLabel1.getText().length() == 13) {
            Management mm = new Management();
            ArrayList<Prodotti> pr;
            try {
                pr = mm.ReadFile();
                for (int i = 0; i < pr.size(); i++) {
                    if (jLabel1.getText().equals(pr.get(i).getBarcode())) {
                        Dati.getInstance().setPr(pr.get(i));
                        JOptionPane.showMessageDialog(new JFrame(), "Barcode trovato", "Error", JOptionPane.INFORMATION_MESSAGE);
                        this.setVisible(false);
                        Cassa cs = new Cassa();                        
                        cs.AddRows(true);
                        cs.setVisible(true);
                        cs.setExtendedState(cs.getExtendedState() | JFrame.MAXIMIZED_BOTH);
                    }
                }
            } catch (FileNotFoundException ex) {
                Logger.getLogger(CodiceProdotto.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(new JFrame(), "Barcode invalido", "Error", JOptionPane.ERROR_MESSAGE);
            jLabel1.setText("?");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

